# Arithmetic
print("Arithmetic operators")
print("5 + 6 = ", 5 + 6)
print("5 - 6 = ", 5 - 6)
print("5 * 6 = ", 5 * 6)
print("5 / 6 = ", 5 / 6)
print("5 ** 6 = ", 5 ** 6)
print("5 // 6 = ", 5 // 6)
print("5 % 6 = ", 5 % 6)

# Assignment
print("Assignment operators")
x = 5
print(x)
x -= 7
print(x)

# Comparison
print("Comparison operators")
i = 5
print(i == 8)
print(i <= 10)
print(i != 12)

